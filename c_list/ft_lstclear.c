#include "../push_swap.h"

void	ft_lstclear(t_list **lst)
{
	t_list	*head;
	t_list	*temp;

	head = *lst;
	if (!lst)
		return ;
	while (*lst)
	{
		temp = (*lst)->next;
		free(*lst);
		*lst = temp;
	}
	*lst = NULL;
}
